package com.example.adviser.vo;

public class Tbl_Deep {

    private String userEmail;
    private String deepResult;
    private String deepImg;

    public Tbl_Deep() {
        this.userEmail = userEmail;
        this.deepResult = deepResult;
        this.deepImg = deepImg;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getDeepResult() {
        return deepResult;
    }

    public void setDeepResult(String deepResult) {
        this.deepResult = deepResult;
    }

    public String getDeepImg() {
        return deepImg;
    }

    public void setDeepImg(String deepImg) {
        this.deepImg = deepImg;
    }
}
